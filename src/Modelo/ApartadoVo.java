package Modelo;


public class ApartadoVo {

private int id_apatado;
private int id_cliente;
private String art;
private String color_art;
private int id_size;
private int amount;
private String registro;
private String plazo;
private String estado;

    public int getId_apatado() {
        return id_apatado;
    }

    public void setId_apatado(int id_apatado) {
        this.id_apatado = id_apatado;
    }

    public int getId_cliente() {
        return id_cliente;
    }

    public void setId_cliente(int id_cliente) {
        this.id_cliente = id_cliente;
    }

    public String getArt() {
        return art;
    }

    public void setArt(String art) {
        this.art = art;
    }

    public String getColor_art() {
        return color_art;
    }

    public void setColor_art(String color_art) {
        this.color_art = color_art;
    }

    public int getId_size() {
        return id_size;
    }

    public void setId_size(int id_size) {
        this.id_size = id_size;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public String getRegistro() {
        return registro;
    }

    public void setRegistro(String registro) {
        this.registro = registro;
    }

    public String getPlazo() {
        return plazo;
    }

    public void setPlazo(String plazo) {
        this.plazo = plazo;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }


    
}
